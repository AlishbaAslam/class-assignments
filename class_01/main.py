# DATA TYPES IN PYTHON:

# 1:Numeric Data Types:

# Integer (int):
age = 22

# Float (float): decimal numbers.
height = 5.3 

# Complex (complex):
a = 5 + 5j 

# 2:Sequence Data Types:

# String (str):
text = "Hello, World!"

# List (list): mutable collection.
list = [3, "Python", 4.16]

# Tuple (tuple): immutable collection.
tuple = (1, 2, "Python")

# 3:Boolean Data Type:

# Boolean (bool):
is_student = True
is_teacher = False

# 4:Set Data Type:

# Set (set): unordered and unique items.
set = {1, 2, 3, 4,}

# Frozen Set (frozenset): immutable set.
frozen_set = frozenset({1, 2, 3, 4})

# 5:Dictionary Data Type:

# Dictionary (dict): key-value pairs.
person = {"name": "Alishba", "age": 22, "city": "Karachi"}

# None Data Type:

# None (None): 
a = None

# Range (range): sequence of numbers.
range = range(5)





